/**********************************************************************************************************
 * strlen.c
 * _strlen()-returns the length of a string
 *
 * return-length of a string
 * **********************************************************************************************************/

#include "main.h"

int _strlen(char *str)
{
	int i;

	for(i=0;*str!='\0';i++)
	{
		str++;
	}
	return(i);
}
